package com.example.model;

/**
 * 我的报名记录实体类
 * 
 * @author admin
 *
 */
public class MyApply {
	int my_apply_img;
	String my_apply_title;
	String my_apply_institutions;
	String my_apply_time;
	String my_apply_day;

	public int getMy_apply_img() {
		return my_apply_img;
	}

	public void setMy_apply_img(int my_apply_img) {
		this.my_apply_img = my_apply_img;
	}

	public String getMy_apply_title() {
		return my_apply_title;
	}

	public void setMy_apply_title(String my_apply_title) {
		this.my_apply_title = my_apply_title;
	}

	public String getMy_apply_institutions() {
		return my_apply_institutions;
	}

	public void setMy_apply_institutions(String my_apply_institutions) {
		this.my_apply_institutions = my_apply_institutions;
	}

	public String getMy_apply_time() {
		return my_apply_time;
	}

	public void setMy_apply_time(String my_apply_time) {
		this.my_apply_time = my_apply_time;
	}

	public String getMy_apply_day() {
		return my_apply_day;
	}

	public void setMy_apply_day(String my_apply_day) {
		this.my_apply_day = my_apply_day;
	}
}
